declare(strict_types=1);

namespace ImDigital\Serverless\Controller\Adminhtml\Function;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;

class Edit extends Action
{
    public const ADMIN_RESOURCE = 'ImDigital_Serverless::serverless_functions';

    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * Index constructor.
     * @param Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * @return \Magento\Framework\View\Result\Page
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('ImDigital_Serverless::manage');
        if ($this->getRequest()->getParam("id")) {
            $resultPage->getConfig()->getTitle()->prepend(__('Edit Serverless Function'));
        } else {
            $resultPage->getConfig()->getTitle()->prepend(__('Add New Serverless Function'));
        }
        return $resultPage;
    }
}
